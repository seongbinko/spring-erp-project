package kr.co.jhta.erp.dto;

import java.util.Date;

public class ProductStockDto {

	private int productNo;
	private String productName;
	private int productPrice;
	private Date productCreateDate;
	private int categoryNo;
	private String categoryName;
	private int storeNo;
	private String storeName;
	private String storeAddress;
	private String storeTel;
	private int amount;
	
	public ProductStockDto() {}

	public int getProductNo() {
		return productNo;
	}

	public void setProductNo(int productNo) {
		this.productNo = productNo;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public int getProductPrice() {
		return productPrice;
	}

	public void setProductPrice(int productPrice) {
		this.productPrice = productPrice;
	}

	public Date getProductCreateDate() {
		return productCreateDate;
	}

	public void setProductCreateDate(Date productCreateDate) {
		this.productCreateDate = productCreateDate;
	}

	public int getCategoryNo() {
		return categoryNo;
	}

	public void setCategoryNo(int categoryNo) {
		this.categoryNo = categoryNo;
	}

	public String getCategoryName() {
		return categoryName;
	}

	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}

	public int getStoreNo() {
		return storeNo;
	}

	public void setStoreNo(int storeNo) {
		this.storeNo = storeNo;
	}

	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	public String getStoreAddress() {
		return storeAddress;
	}

	public void setStoreAddress(String storeAddress) {
		this.storeAddress = storeAddress;
	}

	public String getStoreTel() {
		return storeTel;
	}

	public void setStoreTel(String storeTel) {
		this.storeTel = storeTel;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}

	@Override
	public String toString() {
		return "ProductStockDto [productNo=" + productNo + ", productName=" + productName + ", productPrice="
				+ productPrice + ", productCreateDate=" + productCreateDate + ", categoryNo=" + categoryNo
				+ ", categoryName=" + categoryName + ", storeNo=" + storeNo + ", storeName=" + storeName
				+ ", storeAddress=" + storeAddress + ", storeTel=" + storeTel + ", amount=" + amount + "]";
	}
}
