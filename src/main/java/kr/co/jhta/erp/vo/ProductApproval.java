package kr.co.jhta.erp.vo;

import java.util.Date;

public class ProductApproval {

	private int approvalNo;
	private Date requestDate;
	private Date responseDate;
	private int typeNo;
	private int requesterNo;
	private int responserNo;
	private int orderNo;
	private int factoryOrderNo;
	private String status;
	private String note;

	public ProductApproval() {}

	public int getApprovalNo() {
		return approvalNo;
	}

	public void setApprovalNo(int approvalNo) {
		this.approvalNo = approvalNo;
	}

	public Date getRequestDate() {
		return requestDate;
	}

	public void setRequestDate(Date requestDate) {
		this.requestDate = requestDate;
	}

	public Date getResponseDate() {
		return responseDate;
	}

	public void setResponseDate(Date responseDate) {
		this.responseDate = responseDate;
	}

	public int getTypeNo() {
		return typeNo;
	}

	public void setTypeNo(int typeNo) {
		this.typeNo = typeNo;
	}

	public int getRequesterNo() {
		return requesterNo;
	}

	public void setRequesterNo(int requesterNo) {
		this.requesterNo = requesterNo;
	}

	public int getResponserNo() {
		return responserNo;
	}

	public void setResponserNo(int responserNo) {
		this.responserNo = responserNo;
	}

	public int getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(int orderNo) {
		this.orderNo = orderNo;
	}

	public int getFactoryOrderNo() {
		return factoryOrderNo;
	}

	public void setFactoryOrderNo(int factoryOrderNo) {
		this.factoryOrderNo = factoryOrderNo;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	@Override
	public String toString() {
		return "ProductApproval [approvalNo=" + approvalNo + ", requestDate=" + requestDate + ", responseDate="
				+ responseDate + ", typeNo=" + typeNo + ", requesterNo=" + requesterNo + ", responserNo=" + responserNo
				+ ", orderNo=" + orderNo + ", factoryOrderNo=" + factoryOrderNo + ", status=" + status + ", note="
				+ note + "]";
	}


}