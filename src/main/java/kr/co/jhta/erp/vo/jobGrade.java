package kr.co.jhta.erp.vo;

import org.apache.ibatis.type.Alias;

@Alias("JobGrade")
public class jobGrade {
	private int no;
	private String gradeName;
	private int gradeSalary;
	@Override
	public String toString() {
		return "jobGrade [no=" + no + ", gradeName=" + gradeName + ", gradeSalary=" + gradeSalary + "]";
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getGradeName() {
		return gradeName;
	}
	public void setGradeName(String gradeName) {
		this.gradeName = gradeName;
	}
	public int getGradeSalary() {
		return gradeSalary;
	}
	public void setGradeSalary(int gradeSalary) {
		this.gradeSalary = gradeSalary;
	}
	
	
}
