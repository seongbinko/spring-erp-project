package kr.co.jhta.erp.vo;

import java.util.Date;

import org.apache.ibatis.type.Alias;

@Alias("Attendance")
public class Attendance {
	private int no;
	private Date inTime;
	private Date outTime;
	private String status;
	private int employeeNo;
	private String employeeName;
	private double overtimeAmount;
	private double latetimeAmount;
	private Date attendanceDate;
	private String fixedStatus;
	
	public Attendance() {}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public Date getInTime() {
		return inTime;
	}

	public void setInTime(Date inTime) {
		this.inTime = inTime;
	}

	public Date getOutTime() {
		return outTime;
	}

	public void setOutTime(Date outTime) {
		this.outTime = outTime;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public int getEmployeeNo() {
		return employeeNo;
	}

	public void setEmployeeNo(int employeeNo) {
		this.employeeNo = employeeNo;
	}

	public String getEmployeeName() {
		return employeeName;
	}

	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}

	public double getOvertimeAmount() {
		return overtimeAmount;
	}

	public void setOvertimeAmount(double overtimeAmount) {
		this.overtimeAmount = overtimeAmount;
	}

	public double getLatetimeAmount() {
		return latetimeAmount;
	}

	public void setLatetimeAmount(double latetimeAmount) {
		this.latetimeAmount = latetimeAmount;
	}

	public Date getAttendanceDate() {
		return attendanceDate;
	}

	public void setAttendanceDate(Date attendanceDate) {
		this.attendanceDate = attendanceDate;
	}

	public String getFixedStatus() {
		return fixedStatus;
	}

	public void setFixedStatus(String fixedStatus) {
		this.fixedStatus = fixedStatus;
	}

	@Override
	public String toString() {
		return "Attendance [no=" + no + ", inTime=" + inTime + ", outTime=" + outTime + ", status=" + status
				+ ", employeeNo=" + employeeNo + ", employeeName=" + employeeName + ", overtimeAmount=" + overtimeAmount
				+ ", latetimeAmount=" + latetimeAmount + ", attendanceDate=" + attendanceDate + ", fixedStatus="
				+ fixedStatus + "]";
	}

	
}