package kr.co.jhta.erp.vo;

public class ProductApprovalType {

	private int no;
	private String name;

	public ProductApprovalType() {}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "ProductApprovalType [no=" + no + ", name=" + name + "]";
	}


}
