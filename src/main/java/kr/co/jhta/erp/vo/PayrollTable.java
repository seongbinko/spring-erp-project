package kr.co.jhta.erp.vo;

import org.apache.ibatis.type.Alias;

@Alias("PayrollTable")
public class PayrollTable {
	private Integer payrollNo;
	private String payrollDate;
	private String payrollMonth;
	private String payrollType;
	private Integer payrollAmount;
	private Integer payrollCount;
	
	public PayrollTable() {}

	public Integer getPayrollNo() {
		return payrollNo;
	}

	public void setPayrollNo(Integer payrollNo) {
		this.payrollNo = payrollNo;
	}

	public String getPayrollDate() {
		return payrollDate;
	}

	public void setPayrollDate(String payrollDate) {
		this.payrollDate = payrollDate;
	}

	public String getPayrollMonth() {
		return payrollMonth;
	}

	public void setPayrollMonth(String payrollMonth) {
		this.payrollMonth = payrollMonth;
	}

	public String getPayrollType() {
		return payrollType;
	}

	public void setPayrollType(String payrollType) {
		this.payrollType = payrollType;
	}

	public Integer getPayrollAmount() {
		return payrollAmount;
	}

	public void setPayrollAmount(Integer payrollAmount) {
		this.payrollAmount = payrollAmount;
	}

	public Integer getPayrollCount() {
		return payrollCount;
	}

	public void setPayrollCount(Integer payrollCount) {
		this.payrollCount = payrollCount;
	}

	@Override
	public String toString() {
		return "PayrollTable [payrollNo=" + payrollNo + ", payrollDate=" + payrollDate + ", payrollMonth="
				+ payrollMonth + ", payrollType=" + payrollType + ", payrollAmount=" + payrollAmount + ", payrollCount="
				+ payrollCount + ", getPayrollNo()=" + getPayrollNo() + ", getPayrollDate()=" + getPayrollDate()
				+ ", getPayrollMonth()=" + getPayrollMonth() + ", getPayrollType()=" + getPayrollType()
				+ ", getPayrollAmount()=" + getPayrollAmount() + ", getPayrollCount()=" + getPayrollCount()
				+ ", getClass()=" + getClass() + ", hashCode()=" + hashCode() + ", toString()=" + super.toString()
				+ "]";
	}
	
	
	
}
